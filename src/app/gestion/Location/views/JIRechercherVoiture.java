    private void btnrechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrechercherActionPerformed
        // TODO add your handling code here:
         DefaultTableModel df=(DefaultTableModel) tablev.getModel();
        df.setRowCount(0);
        String req="Select ex from Exemplaire ex join ex.voiture v join v.fabricants f where ";
        int n=0;
        
        if(cbv.isSelected()){
            n++;
            req+=" lower(ex.voiture.nomv) like lower('%"+txtvoiture.getText()+"%') ";
            if(cbf.isSelected()){
                req+=" and lower(f.nomf) like lower('%"+txtfabricant.getText()+"%') ";
            }
            }else if(cbf.isSelected()){
            n++;
             req+=" lower(f.nomf) like lower('%"+txtfabricant.getText()+"%') ";
        }if(n==0 || (txtvoiture.getText().equals("") && txtfabricant.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Veuillez cocher un critere de recherche et remplir les zones de textes.");
            }else{
            Query q=s.createQuery(req);
            List<Exemplaire> l=q.list();
            for(Exemplaire e:l){
                df.addRow(new Object[]{e.getVoiture().getNomv(),e.getVoiture().getFabricants(),e.getMatricule(),e.getType(),
                e.getColor(),e.getPrix(),e.getLocation(),(e.getEtatEx()?"Disponible":"Indisponible")});
            }
        }
    }//GEN-LAST:event_btnrechercherActionPerformed

