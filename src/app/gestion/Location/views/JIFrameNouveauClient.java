    private void btnajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajouterActionPerformed
        // TODO add your handling code here:
        s.beginTransaction();
        Client c=new Client();
        c.setCin(txtcin.getText());
        c.setNomClt(txtnom.getText());
        c.setPrenom(txtprenom.getText());
        c.setAdresse(txtadresse.getText());
        c.setLocation(l);
        c.setEtatCpt(true);
        c.setEmprunte(false);
        s.save(c);
        s.getTransaction().commit();
        JOptionPane.showMessageDialog(null, "Client Ajouter Avec Succes.");
        txtcin.setText("");
        txtnom.setText("");
        txtprenom.setText("");
        txtadresse.setText("");
    }//GEN-LAST:event_btnajouterActionPerformed


